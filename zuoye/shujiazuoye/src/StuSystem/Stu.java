package StuSystem;

public class Stu {
	private int id;
	private String name;
	private char sex;
	private int age;
	private int grade;
	
	public void setID(int id){
		this.id=id;
	}
	public int getID(){
		return id;
	}
	public void setName(String name){
		this.name=name;
	}
	public String getName(){
		return name;
	}
	public void setSex(char sex){
		this.sex=sex;
	}
	public char getSex(){
		return sex;
	}
	public void setAge(int age){
		this.age=age;
	}
	public int getAge(){
		return age;
	}
	public void setGrade(int grade){
		this.grade=grade;
	}
	public int getGrade(){
		return grade;
	}
}
